
public class BinaryNumbers {

	public static void main(String[] args) {
		System.out.println("Decimal \t binary");
		System.out.println("1 \t\t 1");
		System.out.println("2 \t\t 10");
		System.out.println("3 \t\t 11");
		System.out.println("4 \t\t 100");
		System.out.println("5 \t\t 101");
		System.out.println("6 \t\t 110");
		System.out.println("7 \t\t 111");
		System.out.println("8 \t\t 1000");
		

	}

}
